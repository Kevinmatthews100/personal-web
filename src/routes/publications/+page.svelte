<p class="mb-4">
  <i>Horizons</i> takes the listener through the transformation the sky makes from the end of one day, to the beginning the
  of the next. In writing this music, I drew inspiration from my impressions of the literal colors seen when looking at the
  sky during these times of day and night, as well as inspirations from possible symbolic interpretations of this kind of
  change: the end of one thing, transformation, and the start of another.
</p>
<p class="mb-4">Score and parts included: Euphonium 1-5 (BC), Tuba 1-3.</p>
